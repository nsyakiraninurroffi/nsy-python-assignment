# Program Menghitung Energi Kinetik

print("\033[1;34m=====================")
print("====Energi Kinetik====")
print("=====================")

m = float(input("Masukan massa/berat nya: "))
v = float(input("Masukan volumenya: "))

Ek = 1/2 * m * v * v

print("\33[97m======================")
print(f"Jadi Energi Kinetiknya = {round (Ek, 2)} m/s")
print("\33[97m======================\n")