# Program Menghitung Jarak

print("="*40)
kecepatan = int(input("masukkan kecepatan mobil (km/jam)\t: "))
waktu = int(input("masukkan waktu tempuh (jam)\t\t: "))

jarak = kecepatan * waktu
print("="*40)
print(f"Jarak yang di tempuh mobil adalah {jarak} km/jam")
