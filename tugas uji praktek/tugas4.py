# Mengisi variabel dengan nilai ynag diberikan 
angka1 = 87.77
angka2 = 66.50
angka3 = 97.5 

# Menampilkan nilai masing-masing
print(f"Angka 1 : {angka1}")
print(f"Angka 2 : {angka2}")
print(f"Angka 3 : {angka3}")

#Menghitung jumlah rata-rata
jumlah = angka1 + angka2 + angka3
rata_rata = jumlah / 3

# Menampilkan hasil jumlah dan rata-rata 
print(f"Jumlah dari ketiga angka : {jumlah}")
print(f"Jumlah dari rata-rata : {rata_rata}")
      